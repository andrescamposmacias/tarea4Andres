package andres;

/**
 *
 * @author andres
 */
public class Tarea4Andres {
    public int hola;
    public int adios;

    public Tarea4Andres(int hola, int adios) {
        this.hola = hola;
        this.adios = adios;
    }
    
    public Tarea4Andres() {
        this.hola = 8;
        this.adios = 3;
    }

    public int getHola() {
        return hola;
    }

    public void setHola(int hola) {
        this.hola = hola;
    }

    public int getAdios() {
        return adios;
    }

    public void setAdios(int adios) {
        this.adios = adios;
    }
    
    
}
